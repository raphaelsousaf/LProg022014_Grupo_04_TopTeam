public class ModeloFinanceiro {
    
    private String Horarioe;
    private String Horarios;
    private String Dia;
    private String TipoVeiculo;
    private String Pernoite;

    public String getDia() {
        return Dia;
    }

    public void setDia(String Dia) {
        this.Dia = Dia;
    }

    public String getHorarioe() {
        return Horarioe;
    }

    public void setHorarioe(String Horarioe) {
        this.Horarioe = Horarioe;
    }

    public String getHorarios() {
        return Horarios;
    }

    public void setHorarios(String Horarios) {
        this.Horarios = Horarios;
    }

    public String getPernoite() {
        return Pernoite;
    }

    public void setPernoite(String Pernoite) {
        this.Pernoite = Pernoite;
    }

    public String getTipoVeiculo() {
        return TipoVeiculo;
    }

    public void setTipoVeiculo(String TipoVeiculo) {
        this.TipoVeiculo = TipoVeiculo;
    }
    
    
    
    
       
    
}
